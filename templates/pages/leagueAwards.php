<h4>IHSLA Open Forum Meeting</h4>


<p>In an effort to increase communication between our member's and leadership we have scheduled our third annual open forum membership meeting the Saturday of the IHSLA State Championships. This is an opportunity for team coaches and representatives to discuss league issues. This is not a mandatory meeting but all teams are welcome to attend. If there is a specific item you would like added to the agenda please email me prior to the meeting.</p> 


<p>The meeting will be held from 3:00 – 4:00pm and will be held at Zionsville High School's LGI. Attendees should enter door #41 (directly across the parking lot from the stadium entrance).</p>


<h4>IHSLA Awards Ceremony</h4>


<p>All student athletes who have earned All-State (including academic awards) are invited to be recognized on field prior to the IHSLA State Championship Game at Zionsville High School on Saturday May 24th beginning at 5:45pm. All award winners should meet behind the press box bleachers at 5:20pm. All State players will be recognized individually. Due to the number of recipients and time constraints, all academic and academic all state winners will be recognized as a group. </p> 