<h3>Officials:</h3>
<p>The Indiana High School Lacrosse Association subcontracts the officials for it's games using the <a href="http://www.InLaxRef.org">Indiana Lacrosse Officials Association (ILOA)</a>.  They use the Arbiter Sports website to schedule all games. Each team is required to have an Arbiter Sports account and should double check the Arbiter Sports site before your game to assure officials have been assigned to it. 
   Please visit their web site <a href="http://www.InLaxRef.org"> www.InLaxRef.org</a> for more information about how to contact officials, official fees, cancelation polices, 
   and how to get an Arbiter Sports account.</p>

